            <h4>What is Blood Bank Management System?</h4>
            <p>
            Blood bank is a place where blood bag that is collected from blood donation events is stored in one place. The term “blood bank” refers to a division of a hospital laboratory where the storage of blood product occurs and where proper testing is performed to reduce the risk of transfusion related events . The process of managing the blood bag that is received from the blood donation events needs a proper and systematic management. The blood bag must be handled with care and treated thoroughly as it is related to someone’s life. The development of Web-based Blood Bank Management System (BBMS) is proposed to provide a management functional to the blood bank in order to handle the blood bag and to make entries of the individuals who want to donate blood and who are in need.   
        </p>
            <p>
            Over the years, blood banking has helped save countless lives. Most blood banks, including the American Red Cross, rely on volunteer blood donors. During a typical blood donation, about 1 pint of blood is collected, along with a few small tubes for testing. The tubes and donation are labeled with donor information and sent off for processing and testing.Next, the donated blood is taken to the processing center. Whole blood is usually centrifuged, which separates it into its many components that patients may need: red blood cells, platelets and plasma. White blood cells are removed from the blood to minimize the chance of a reaction in the recipient.While the donation is being processed, the blood test tubes are sent out for screening. At the American Red Cross, this includes testing for diseases such as hepatitis B, hepatitis C, HIV and West Nile Virus. If any of the tests are positive, the donor will be notified and the blood will not be used.
During the testing stage, the blood is also typed for ABO groups and Rh factor. Once the blood is screened and processed, it is stored up to 42 days or until it is needed. A blood bank ensures that hospitals have a safe, reliable blood supply for patients in need. You can help meet supply demands by scheduling your donation today. Your donation may even help save more than one life.        </p>
