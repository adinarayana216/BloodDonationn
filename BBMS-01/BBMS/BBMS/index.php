            <h4>Donor Tips</h4>
            <p>
                Blood donation is a voluntary procedure that can help save lives. There are several types of blood donation. Each type helps meet different medical needs.  
            </p>
            <p>
            Whole blood donation is the most common type of blood donation. During this donation, you donate about a pint (about half a liter) of whole blood. The blood is then separated into its components — red cells, plasma and sometimes platelets.
            </p>
            <p>
            <strong>Eligibility requirements:</strong>
            <br>
            <br>
 To be eligible to donate whole blood, plasma or platelets, you must be:<br>
<ul>
<li>In good health.</li>
<li>At least 16 or 17 years old, depending on the law in your state. Some states allow legal minors to donate with parent permission. While there's no legal upper age limit, policies may vary between individual donor centers.</li>
<li>At least 110 pounds (about 50 kilograms).</li>
<li>Able to pass the physical and health-history assessments.</li>
<li>Eligibility requirements differ slightly among different types of blood donation.</li>
            </p>
