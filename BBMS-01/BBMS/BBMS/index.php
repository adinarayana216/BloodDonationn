            <p>
            <strong>Food and medications:</strong><br>
            <br>
Before your blood donation:<br>
            
<ul>
<li>Get plenty of sleep the night before you plan to donate.</li>
<li>Eat a healthy meal before your donation. Avoid fatty foods, such as a hamburger, fries or ice cream.</li>
<li>Drink plenty of water before the donation.</li>
<li>Check to see if any medications you are taking or recently took would prevent you from donating. For example, if you are a platelet donor, you must not take aspirin for two days prior to donating. Talk to a member of your health care team before you stop taking any medications in order to donate blood.</li>
<li>Wear a shirt with sleeves that can be rolled up.</li>
            </p>
